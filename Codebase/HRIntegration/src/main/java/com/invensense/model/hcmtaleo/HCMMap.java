package com.invensense.model.hcmtaleo;

public class HCMMap {
	private String hcmGuid;
	private String hcmKey;
	private String srcData;
	private String description;
	public String getHcmGuid() {
		return hcmGuid;
	}
	public void setHcmGuid(String hcmGuid) {
		this.hcmGuid = hcmGuid;
	}
	public String getHcmKey() {
		return hcmKey;
	}
	public void setHcmKey(String hcmKey) {
		this.hcmKey = hcmKey;
	}
	public String getSrcData() {
		return srcData;
	}
	public void setSrcData(String srcData) {
		this.srcData = srcData;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	

}
